                        <h5>PUI Components</h5>
                    </header>
                     <p><strong>Physical:</strong> Based on urban interventions and strengthened by community participation. Includes the construction and improvement of public spaces, housing, and public buildings, and restoration of the environment.</p>
                     <p><strong>Social:</strong> Relying on a strategy of identifying problems and opportunities, establishing and approving projects, and using participatory design practices such as public workshops. Combined with strengthened community organization and leadership promotion, this allows for restoration of the social fabric.</p>
                     <p><strong>Institutional:</strong> Involving the coordination of activities carried out by all elements of the municipality in a particular zone. In addition, alliances with the private sector, NGOs, national and international bodies, and community organizations are promoted.</p>
