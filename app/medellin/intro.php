                <section id="marginality">
                    <header><h5>MARGINALITY IN MEDELLI'N</h5></header>
                    <p>The process of "informalization," understood as the formation of precarious neighborhoods, has been a characteristic of Medelli'n’s history throughout the twentieth and into the twenty-first century, a consequence of the continuous migration toward the city.</p>

                    <p>The effects of this demographic growth became evident at the beginning of the last century, in the considerable rise in the demand for housing. This heightened demand was associated with the production of working-class residences for the huge numbers of manual laborers needed in the emerging industrial sector.1 From that time on, neighborhoods created through both public and private initiatives began appearing, primarily near the northeastern bank and along the tram route and principal roads. The public residences were the product of institutions such as the Instituto Cre'dito Territorial and the Banco Central Hipotecario, while the private residences represent investments by local landowners.</p>
        
                    <p>Despite these significant public and private efforts, the demand for housing continued to grow in the following decades. A migratory wave following the rural displacement and political violence of the 1950s pushed the city’s annual growth rate to 6 percent.</p>
                    <p>
                    In ten years Medelli'n doubled its population; informal squatter settlements (urbanizaciones piratas) and illegal neighborhoods (barrios de invasio'n) began to appear in the most inaccessible peripheral areas. From this period we find the examples of Popular, Santo Domingo, and Granizal toward the eastern bank, and Doce de Octubre and Picacho toward the western bank.
                    </p>
                    <p>
                    Increasing in intensity, the new urbanizing dynamic began to generate profound segregation within the city’s physical, social, and economic western slopes, the informal city began to emerge; here one finds the ad hoc residences of the poor. Parallel to these areas, the middle and upper classes occupy the center and south of the valley, in the planned formal city. Medelli'n’s path diverged into two realities—two opposing "cities"—dramatically segregated by location and geographical relief.
                    </p>
