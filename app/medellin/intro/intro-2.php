            In concert with the MetroCable, the PUI has made a significant
            contribution toward improving the quality of life for approximately 170,000 residents experiencing severe social inequality, poverty, and violence.
            
            <p>
            The PUI leverages the economic benefits of new mobility infrastructure to incorporate marginalized communities into the city. By reducing travel times to the city center from over an hour to less than ten minutes, the MetroCable has enhanced access to employment opportunities and eroded the boundary between the formal and informal city.
            </p>

            <p>
            Working with the community to conceptualize, develop,
            and construct new open-space networks, the designers of the PUI have sensitively integrated a mobility infrastructure of the strategic goals of large and socially complex projects by developing processes that promote ownership by the community.
