                        <h5>Integral urban project methodology</h5>
                    </header>
                    <p>The Proyecto Urbano Integral (PUI)/Integral Urban Project is an instrument for planning and physical intervention in areas characterized by a high degree of marginalization, segregation, poverty, and violence. PUIs are targeted to solve speciﬁc problems in a territory where the state has generally been absent.</p>
                    <p>The Integral Urban Project strives to integrate community participation, inter-institutional coordination, promotion of housing, and the improvement of public space and mobility. In addition, the PUI promotes support for collective amenities and the restoration of the environment.</p>
                    <p>With these criteria, the northeastern communities of Medellín provided a fitting test for the pilot project. These sectors of the city had the lowest ratings on the quality-of-life index and index of human development.  At the same time, in the same area, there was the launch of the Metrocable, a system of transportation that connected these informal settlements to the city Metro system by cable car. The PUI helped to select the location of the stations, with the objective of complementing and amplifying the positive impact of the Metrocable.</p>
                    <p>The PUI implemented a process of neighborhood consolidation through a series of projects that generated both increased accessibility and greater structure in the region. These projects were characterized by community amenities—parks, streets, walkways, and pedestrian bridges that connected neighborhoods. The PUI northeast is focused on improving public infrastructure as a means of social transformation, primarily in the densely populated areas of the city that formed in the 1950s, largely through extralegal urbanization.</p>
                    <p>The methodology applied in the northeastern PUI was gradually developed during the project. This method is similar to those used in comparable projects: first comes identification of the issues, next a diagnosis, and then a formulation of strategies to solve or minimize the problems. In addition to the high degree of institutional coordination, this project was innovative because of its sustainability phase; this phase began parallel with the intervention and continues after it, involving the community as an active part of the process from the start.</p>
