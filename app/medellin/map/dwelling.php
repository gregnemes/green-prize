            <h5>DWELLING CONSOLIDATION AND ENVIRONMENTAL RESTORATION IN THE JUAN BOBO STREAM</h5>
        </header>
        <p>
        The improvement in these territories with low levels of consolidation was a crucial part of the policy of social urbanism. As a complement to the northeastern Integral Urban Project that focused construction on new spaces of encounter and public buildings, the Pilot Project for Habitat Consolidation in the Juan Bobo stream  was identified as the first model of housing intervention in "informal urban ecosystems."
        </p>
        <p>
        The objective of this intervention is to respond to the precarious conditions of both dwellings and the environment by implementing physically, economically, and socially sustainable actions to prevent further unsafe occupation of the area. It is designed to improve the housing standard and restore urban and natural environments.
        </p>
        <p>More than ten small homes were built for relocated families. Where houses were well located and reasonably well built, they were improved. Some residences were replaced on site, which allowed for formation of more regular plots. Parallel to these initiatives, retaining walls were built to mitigate the risk of landslides. The territory was equipped with public-service networks highlighting the need to address pollution in the stream. Additionally, pathways, bridges, and public spaces were built as a way of upgrading the surrounding area and improving accessibility. Protective zones were set up for the preservation of the existing vegetative layer.</p>

        Intervention strategies:
        <ul>
            <li>Liberating the stream channel.</li>
            <li>Distributing land at the mid-slope level.</li>
            <li>Increasing density at the high slope level.</li>
        </ul>
        
        Results
        <p>
        Today, thanks to community initiative, those living in the area under the Juan Bobo ravine (New Sun Eastern Sector) have garbage collection, water and sewer services, improved mobility, public spaces, squares, trails, stairways, gazebos, and pedestrian bridges. These amenities raise the public-space index from 0.5 square meters per capita to 3.17 square meters per capita. The inhabitants enjoy greater safety, improved structural stability, more parks, and better sanitation.</p>
        
        <p>Inhabitants now have titles to their homes, creating a basis for access to credit in the formal sector as well as generating lease revenue. The increase in construction height provided 108 families with at least 30 square meters per house and the technical ability for further development.</p>
