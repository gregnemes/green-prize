            <h5>PUENTE MIRADOR: ANDALUCÍA-LA FRANCIA BRIDGE</h5>
        </header>
        <p>The addition of pedestrian bridges is intended to improve connectivity between neighborhoods, often limited by the large number of streams and natural basins found in the area. This ﬁrst bridge, located over the La Herrera stream, improves travel times to and from the Andalucía Metrocable station and has become a new public space and meeting place between previously separate areas.</p>

        Some signiﬁcant urbanistic components include:
        <ul>
            <li>The creation of a “park bridge” used as an observation point and a new visual referent for the area.</li>
            <li>Improvement of the section of road along 48A connecting to the promenade on 107th Street.</li>
            <li>Improvement and connection of street frontage, eliminating barriers and grates to indicate that public space is everyone’s property.</li>
            <li>Construction of two new parks along the north part of the bridge and under it on La Herrera stream.</li>
            <li>The addition of the Mirador (“lookout”) bridge—named by the community—which quickly became a physical and social landmark for commune 2.</li>
        </ul>
